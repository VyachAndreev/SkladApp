package com.andreev.skladapp.data

abstract class AbstractPosition(
    val comment: String? = null,
    val createdFrom: Long? = null,
    val date: String? = null,
    val diameter: String? = null,
    val id: Long? = null,
    val manufacturer: String? = null,
    val mark: String? = null,
    val packing: String? = null,
    val part: String? = null,
    val plav: String? = null,
    val status: String? = null,
    val type: String? = null,
    val positionsList: Array<Position>? = null,
    val location: String? = null,
)

class Position(
    comment: String?,
    createdFrom: Long?,
    date: String?,
    diameter: String?,
    id: Long?,
    manufacturer: String?,
    mark: String?,
    val mass: String? = null,
    packing: String?,
    part: String?,
    plav: String?,
    status: String?,
    type: String?,
    positionsList: Array<Position>?,
    location: String?,
): AbstractPosition (
    comment,
    createdFrom,
    date,
    diameter,
    id,
    manufacturer,
    mark,
    packing,
    part,
    plav,
    status,
    type,
    positionsList,
    location
)

